						Setiap e-commerce memiliki ulasan atau feedback untuk setiap produknya. Ulasan atau feedback produk berupa komentar dari pembeli sebelumnya bisa positif atau negatif. Mulai dari pengiriman yang cepat hingga pilihan pembayaran yang aman. Tidak sedikit pembeli yang trauma dalam belanja online akibat ulah oknum yang tidak bertanggungjawab dalam memberikan pelayanan yang berbelanja online, mulai dari barang yang tidak dikirim padahal sudah melakukan transfer hingga barang yang dikirimkan tidak sesuai dengan deskripsi produk yang diberikan. Pentingnya ulasan produk adalah untuk mengetahui kuliatas produk dan pelayanannya. Serta mengetahui apakah e-commerce atau lapak tersebut terpercaya atau tidak.
